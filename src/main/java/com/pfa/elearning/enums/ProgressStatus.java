package com.pfa.elearning.enums;

/**
 * Enumeration of progress statuses for tracking learner advancement.
 * 
 * - NOT_STARTED: Content/chapter not yet accessed
 * - IN_PROGRESS: Currently being worked on
 * - COMPLETED: Fully completed
 */
public enum ProgressStatus {
    NOT_STARTED,
    IN_PROGRESS,
    COMPLETED
}
